# -*- coding: UTF-8 -*-
class User:
    """ 用户信息 """
    def __init__(self, uid, uage, uheight, ueducation, usalary, uweight,
                 uhouse, uzodiac, ubtype, ustar, unation, ucar,
                 uadress, uhousehold, unickname, ucharisma):
        self.uid = uid   # 用户id
        self.uage = uage  # 用户年龄
        self.uheight = uheight  # 身高
        self.ueducation = ueducation  # 学历
        self.usalary = usalary  # 工资
        self.uweight = uweight  # 体重
        self.uhouse = uhouse  # 购房情况
        self.uzodiac = uzodiac  # 生肖
        self.ubtype = ubtype  # 血型
        self.ustar = ustar  # 星座
        self.unation = unation  # 民族
        self.ucar = ucar  # 购车情况
        self.uadress = uadress  # 居住地
        self.uhousehold = uhousehold  # 籍贯
        self.unickname = unickname  # 昵称
        self.ucharisma = ucharisma  # 魅力值








